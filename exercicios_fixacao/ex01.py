"""
2. Estruturas condicionais
- Crie uma variável chamada “nota” e atribua a ela um valor entre 0 e 10.
- Escreva um programa que verifique se a nota é maior ou igual a 7.
- Se a nota for maior ou igual a 7, imprima na tela “Aprovado”.
- Caso contrário, imprima na tela “Reprovado”.
"""

nota = int(input("Digite a nota: "))

if nota >= 7:
    print("Aprovado")
else:
    print("Reprovado")
